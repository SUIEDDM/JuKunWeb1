<script setup>
import { reactive, ref } from 'vue'
import { useToastStore } from '../stores/toast'

const toast = useToastStore()

const form = reactive({
  name: '',
  phone: '',
  email: '',
  message: ''
})

const errors = ref({})
const isSubmitting = ref(false)
const isSubmitted = ref(false)

// Formspree 表单端点（请替换为你自己的 endpoint）
// 注册 https://formspree.io/ 获取免费 endpoint
const FORM_ENDPOINT = 'https://formspree.io/f/YOUR_FORM_ID'

const validateForm = () => {
  errors.value = {}

  if (!form.name.trim()) {
    errors.value.name = '请输入您的姓名'
  }

  if (!form.phone.trim()) {
    errors.value.phone = '请输入联系电话'
  } else if (!/^1[3-9]\d{9}$/.test(form.phone)) {
    errors.value.phone = '请输入有效的手机号码'
  }

  if (!form.email.trim()) {
    errors.value.email = '请输入电子邮箱'
  } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(form.email)) {
    errors.value.email = '请输入有效的邮箱地址'
  }

  if (!form.message.trim()) {
    errors.value.message = '请输入留言内容'
  }

  return Object.keys(errors.value).length === 0
}

const handleSubmit = async () => {
  if (!validateForm()) return

  isSubmitting.value = true

  try {
    const response = await fetch(FORM_ENDPOINT, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Accept': 'application/json'
      },
      body: JSON.stringify({
        name: form.name,
        phone: form.phone,
        email: form.email,
        message: form.message
      })
    })

    if (response.ok) {
      isSubmitted.value = true
      toast.success('提交成功', '感谢您的留言！我们会尽快与您联系。')
      form.name = ''
      form.phone = ''
      form.email = ''
      form.message = ''
    } else {
      throw new Error('提交失败')
    }
  } catch (error) {
    toast.error('提交失败', '请稍后重试或直接拨打电话联系我们。')
  } finally {
    isSubmitting.value = false
  }
}
</script>
